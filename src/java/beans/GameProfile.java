package beans;

/**
 *
 * @author betoesquivel
 */
public class GameProfile {
    private int id;
    private String name;
    private int userId;

    public GameProfile() {
    }

    public GameProfile(int id, String name, int userId) {
        this.id = id;
        this.name = name;
        this.userId = userId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
